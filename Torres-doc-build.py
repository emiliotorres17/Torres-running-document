#-------------------------------------------------------------------------#
# Python packages                                                         #
#-------------------------------------------------------------------------#
import os
import sys
from subprocess import call
import time
#=========================================================================#
# Main                                                                    #
#=========================================================================#
if __name__ == "__main__":
    #---------------------------------------------------------------------#
    # Main preamble                                                       #
    #---------------------------------------------------------------------#
    call(["clear"])
    sep     = os.sep
    pwd     = os.getcwd()
    py_path = pwd + "%cpython%c"            %(sep, sep)
    #---------------------------------------------------------------------#
    # Running Python                                                      #
    #---------------------------------------------------------------------#
    os.chdir(py_path)
    pyfile  = "enstrophy-production-2.py"
    call(["python3", pyfile])
    print("**** Successful Python run ****")
    time.sleep(3)
    os.chdir(pwd)
    #---------------------------------------------------------------------#
    # Running LaTeX                                                       #
    #---------------------------------------------------------------------#
    latex_file  = "Torres-doc.tex"
    call(["pdflatex", latex_file])
    call(["pdflatex", latex_file])
    call(["pdflatex", latex_file])
    call(["clear"])
    print("**** Successful LaTeX Run ****")
    time.sleep(1)
