#!/usr/bin/env python3
"""========================================================================
Purpose:
    The purpose of this script is generate the c_{7} term for the subgrid
    stress representation.

Author:
    Emilio Torres
========================================================================"""
#=========================================================================#
# Preamble                                                                #
#=========================================================================#
#-------------------------------------------------------------------------#
# Python packages                                                         #
#-------------------------------------------------------------------------#
import os
import sys
from subprocess import call
#=========================================================================#
# Main                                                                    #
#=========================================================================#
if __name__ == "__main__":
    #---------------------------------------------------------------------#
    # Main preamble                                                       #
    #---------------------------------------------------------------------#
    call(["clear"])
    sep             = os.sep
    pwd             = os.getcwd()
    latex_path      = pwd + "%clatex-inputs%c"          %(sep, sep) 
    #---------------------------------------------------------------------#
    # S11 term                                                            #
    #---------------------------------------------------------------------#
    s11     = "S_{11}("
    for k in range(1,4):
        for l in range(1,4):
            for m in range(1,4):
                s11     += "R_{1%i}"                %(k)
                s11     += "S_{%i%i}"               %(k,l)
                s11     += "R_{%i%i}"               %(l,m)
                s11     += "R_{%i1}"                %(m)
            s11 += "\n"
    s11 += ")"
    f   = open(latex_path + "c7-s11.tex", 'w')
    f.write(s11)
    f.close()
    #---------------------------------------------------------------------#
    # S22 term                                                            #
    #---------------------------------------------------------------------#
    s11     = "S_{22}("
    for k in range(1,4):
        for l in range(1,4):
            for m in range(1,4):
                s11     += "R_{2%i}"                %(k)
                s11     += "S_{%i%i}"               %(k,l)
                s11     += "R_{%i%i}"               %(l,m)
                s11     += "R_{%i2} +"              %(m)
            s11 += "\n"
    s11 += ")"
    f   = open(latex_path + "c7-s22.tex", 'w')
    f.write(s11)
    f.close()
    #---------------------------------------------------------------------#
    # S33 term                                                            #
    #---------------------------------------------------------------------#
    s11     = "S_{33}("
    for k in range(1,4):
        for l in range(1,4):
            for m in range(1,4):
                s11     += "R_{3%i}"                %(k)
                s11     += "S_{%i%i}"               %(k,l)
                s11     += "R_{%i%i}"               %(l,m)
                s11     += "R_{%i3} +"              %(m)
            s11 += "\n"
    s11 += ")"
    f   = open(latex_path + "c7-s33.tex", 'w')
    f.write(s11)
    f.close()
